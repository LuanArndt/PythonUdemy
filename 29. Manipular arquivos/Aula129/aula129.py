# MODOS:
# "r" - Ler - Valor padrão. Abre um arquivo para leitura, erro se o arquivo não existe.

# "a" - Anexar - Abre um arquivo para anexar, cria o arquivo se ele não existe.

# "w" - Gravar - abre um arquivo para gravação, cria o arquivo se ele não existir (Se existir, ele sobescreve o conteudo).

# "x" - Criar - Cria o arquivo especificado, retorna erro se o arquivo existir.

# TIPOS
# "t" - Texto - Valor padrão. Modo texto
# "b" - Binário - Modo binário (por exemplo, imagens)